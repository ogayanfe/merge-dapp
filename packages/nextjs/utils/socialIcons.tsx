export const getSocialIcon = (key: string) => {
  const lowerKey = key.toLowerCase();

  // Specific mappings for keys that might not match directly or have multiple names
  if (lowerKey === "twitter" || lowerKey === "x" || lowerKey.includes("x.com"))
    return ICONS.x({ className: "w-4 h-4" });
  if (lowerKey.includes("github")) return ICONS.github({ className: "w-4 h-4" });
  if (lowerKey.includes("discord")) return ICONS.discord({ className: "w-4 h-4" });
  if (lowerKey.includes("telegram")) return ICONS.telegram({ className: "w-4 h-4" });
  if (lowerKey.includes("bitbucket")) return ICONS.bitbucket({ className: "w-4 h-4" });
  if (lowerKey.includes("codewars")) return ICONS.codewars({ className: "w-4 h-4" });
  if (lowerKey.includes("leetcode")) return ICONS.leetcode({ className: "w-4 h-4" });
  if (lowerKey.includes("gmail") || lowerKey.includes("mail")) return ICONS.gmail({ className: "w-4 h-4" });
  if (lowerKey.includes("facebook")) return ICONS.facebook({ className: "w-4 h-4" });
  if (lowerKey.includes("instagram")) return ICONS.instagram({ className: "w-4 h-4" });
  if (lowerKey.includes("youtube")) return ICONS.youtube({ className: "w-4 h-4" });

  // Direct match if available
  if (ICONS[lowerKey]) {
    const Icon = ICONS[lowerKey];
    return <Icon className="w-4 h-4" />;
  }

  // Fallback to text
  return <span className="text-[10px] font-black uppercase">{key}</span>;
};
